                });
                console.log('Modal initialized successfully');
            } catch (error) {
                console.error('Error initializing modal:', error);
                return;
            }

            // Tangkap elemen untuk validasi
            const jumlahPesan = document.getElementById('jumlahPesan');
            const jumlahError = document.getElementById('jumlahError');
            const konfirmasiPesan = document.getElementById('konfirmasiPesan');

            // Debugging: Log semua tombol pesan
            const pesanButtons = document.querySelectorAll('.pesan-sekarang');
            console.log('Found', pesanButtons.length, 'pesan-sekarang buttons');

            // Fungsi untuk menampilkan error
            const showError = (message) => {
                jumlahError.textContent = message;
                jumlahPesan.classList.add('is-invalid');
                jumlahPesan.focus();
            };

            // Fungsi untuk menghapus error
            const clearError = () => {
                jumlahError.textContent = '';
                jumlahPesan.classList.remove('is-invalid');
            };

            // Tambahkan event listener untuk setiap tombol pesan
            pesanButtons.forEach(button => {
                button.addEventListener('click', (e) => {
                    e.preventDefault();
                    console.log('Pesan button clicked:', button.dataset.nama);

                    // Ambil data dari atribut data
                    try {
                        const nama = button.dataset.nama;
                        const harga = button.dataset.harga;
                        const deskripsi = button.dataset.deskripsi;
                        const gambar = button.dataset.gambar;

                        if (!nama || !harga || !deskripsi || !gambar) {
                            throw new Error('Atribut data tidak lengkap pada tombol');
                        }

                        // Isi modal dengan data
                        document.getElementById('modalNama').textContent = nama;
                        document.getElementById('modalHarga').textContent = harga;
                        document.getElementById('modalDeskripsi').textContent = deskripsi;
                        document.getElementById('modalGambar').src = gambar;

                        // Reset form
                        jumlahPesan.value = 1;
                        document.getElementById('catatan').value = '';
                        clearError();

                        // Tampilkan modal
                        pesanModal.show();
                    } catch (error) {
                        console.error('Error populating modal:', error);
                    }
                });

                // Tambahkan aksesibilitas untuk keyboard
                button.addEventListener('keydown', (e) => {
                    if (e.key === 'Enter' || e.key === ' ') {
                        e.preventDefault();
                        button.click();
                    }
                });
            });

            // Tombol tambah dan kurang jumlah
            document.getElementById('tambahJumlah').addEventListener('click', () => {
                jumlahPesan.value = parseInt(jumlahPesan.value) + 1;
                clearError();
            });

            document.getElementById('kurangJumlah').addEventListener('click', () => {
                if (parseInt(jumlahPesan.value) > 1) {
                    jumlahPesan.value = parseInt(jumlahPesan.value) - 1;
                    clearError();
                }
            });

            // Validasi saat input jumlah berubah
            jumlahPesan.addEventListener('input', () => {
                const value = parseInt(jumlahPesan.value);
                if (isNaN(value) || value < 1) {
                    showError('Jumlah pesanan minimal 1.');
                } else {
                    clearError();
                }
            });

            // Tombol konfirmasi pesanan
            konfirmasiPesan.addEventListener('click', () => {
                console.log('Konfirmasi pesan clicked');

                const nama = document.getElementById('modalNama').textContent;
                const harga = document.getElementById('modalHarga').textContent;
                const jumlah = parseInt(jumlahPesan.value);
                const catatan = document.getElementById('catatan').value;

                // Validasi jumlah
                if (isNaN(jumlah) || jumlah < 1) {
                    showError('Mohon masukkan jumlah pesanan minimal 1.');
                    return;
                }

                // Kirim pesan WhatsApp
                const whatsappNumber = '6282388486060';
                const message = `Halo Blossis, saya ingin memesan:\n\n*${nama}*\nHarga: ${harga}\nJumlah: ${jumlah}\nCatatan: ${catatan || '-'}\n\nApakah bisa diproses?`;
                const encodedMessage = encodeURIComponent(message);
                const whatsappUrl = `https://wa.me/${whatsappNumber}?text=${encodedMessage}`;

                // Buka WhatsApp
                window.open(whatsappUrl, '_blank');

                // Tutup modal
                pesanModal.hide();

                // Tampilkan notifikasi sukses
                const successToast = document.createElement('div');
                successToast.className = 'position-fixed bottom-0 end-0 p-3';
                successToast.style.zIndex = '2000';
                successToast.innerHTML = `
                    <div class="toast" role="alert" aria-live="assertive" aria-atomic="true" data-bs-autohide="true" data-bs-delay="3000">
                        <div class="toast-header bg-success text-white">
                            <strong class="me-auto">Pesanan Terkirim</strong>
                            <button type="button" class="btn-close btn-close-white" data-bs-dismiss="toast" aria-label="Close"></button>
                        </div>
                        <div class="toast-body">
                            Pesanan Anda telah dikirim ke WhatsApp. Terima kasih!
                        </div>
                    </div>
                `;
                document.body.appendChild(successToast);
                try {
                    const toast = new bootstrap.Toast(successToast.querySelector('.toast'));
                    toast.show();
                } catch (error) {
                    console.error('Error showing toast:', error);
                }
            });

            // Smooth scrolling for anchor links
            document.querySelectorAll('a[href^="#"]').forEach(anchor => {
                anchor.addEventListener('click', (e) => {
                    e.preventDefault();
                    const target = document.querySelector(anchor.getAttribute('href'));
                    if (target) {
                        target.scrollIntoView({
                            behavior: 'smooth'
                        });
                    }
                });
            });

            // Navbar scroll effect
            window.addEventListener('scroll', () => {
                const navbar = document.querySelector('.navbar');
                navbar.classList.toggle('scrolled', window.scrollY > 50);
            });

            // Animation on scroll for product and testimonial cards
            const animateOnScroll = () => {
                const elements = document.querySelectorAll('.product-card, .testimonial-card');
                elements.forEach((element, index) => {
                    const elementPosition = element.getBoundingClientRect().top;
                    const screenPosition = window.innerHeight * 0.8;
                    if (elementPosition < screenPosition && !element.classList.contains('visible')) {
                        setTimeout(() => {
                            element.classList.add('visible');
                        }, index * 100);
                    }
                });
            };

            window.addEventListener('scroll', () => {
                requestAnimationFrame(animateOnScroll);
            });
            animateOnScroll();

            // Create floating bubbles dynamically (limited to 2 per section)
            const createBubbles = () => {
                const sections = document.querySelectorAll('section');
                sections.forEach(section => {
                    const bubbleCount = Math.min(2, window.innerWidth < 576 ? 1 : 2); // Kurangi bubble di mobile
                    for (let i = 0; i < bubbleCount; i++) {
                        const bubble = document.createElement('div');
                        bubble.classList.add('bubble', 'bubble-float');

                        const size = Math.floor(Math.random() * 80) + 40;
                        const top = Math.floor(Math.random() * 80);
                        const left = Math.floor(Math.random() * 90);
                        const opacity = Math.random() * 0.15 + 0.05;
                        const colors = [
                            `rgba(255, 61, 104, ${opacity})`,
                            `rgba(255, 179, 71, ${opacity})`,
                            `rgba(167, 255, 131, ${opacity})`,
                            `rgba(255, 138, 91, ${opacity})`
                        ];
                        const color = colors[Math.floor(Math.random() * colors.length)];

                        bubble.style.width = `${size}px`;
                        bubble.style.height = `${size}px`;
                        bubble.style.top = `${top}%`;
                        bubble.style.left = `${left}%`;
                        bubble.style.background = color;

                        const animationDuration = Math.floor(Math.random() * 15) + 8;
                        const animationDelay = Math.floor(Math.random() * 3);
                        bubble.style.animation = `float ${animationDuration}s ease-in-out ${animationDelay}s infinite`;

                        section.appendChild(bubble);
                    }
                });
            };

            createBubbles();

            // Hide preloader on page load
            window.addEventListener('load', () => {
                const preloader = document.getElementById('preloader');
                if (preloader) {
                    preloader.style.opacity = '0';
                    setTimeout(() => {
                        preloader.style.display = 'none';
                    }, 500);
                }
            });
        });
    </script>
